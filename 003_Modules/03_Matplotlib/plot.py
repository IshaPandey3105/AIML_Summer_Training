import matplotlib.pyplot as plt
x=[1,2,3,4]
y=[10,20,30,40]
plt.plot(x, y)                 # Plot line chart
plt.title("Simple Line Graph") # Add a title
plt.xlabel("X Axis")           # Label X
plt.ylabel("Y Axis")           # Label Y
plt.grid(True)                 # Show grid
plt.show()                     # Display the plot